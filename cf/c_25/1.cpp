void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    int mini = 1e9;
    for (int i = 0; i < n - 1; i++)
    {
        mini = min(mini, max(a[i], a[i + 1]));
    }
    cout << mini - 1 << endl;
}
int main()
{
    int t;
    cin >> t;
    while (t > 0)
    {
        solve();
        t--;
    }

    return 0;
}
