void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    sort(a.begin(), a.end());
    int s = 0, e = n - 1;
    int ans = a[0];
    while (s < e)
    {
        int mid = ((s + e) / 2);
        ans = max(ans, a[mid]);
        s = mid + 1;
    }
    cout << ans << endl;
}
int main()
{
    int t;
    cin >> t;
    while (t > 0)
    {
        solve();
        t--;
    }

    return 0;
}
